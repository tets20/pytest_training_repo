    def open_add_new_page(self):
        wd = self.wd
        wd.find_element_by_link_text("add new").click()

    def go_to_home_page(self):
        wd = self.wd
        wd.find_element_by_link_text("home").click()

    def destroy(self):
        self.wd.quit()
